    private void init() {
        // Initialize the SanphamBUS
        sanphamBUS = new SanphamBUS();

        // Get the table model
        tableModel = (DefaultTableModel) tableSanPham.getModel();

        // Load products
        loadSanPham();
    }

    private void loadSanPham() {
        // Get all products
        listSanpham = sanphamBUS.getList();

        // Clear the table
        tableModel.setRowCount(0);

        // Add products to the table
        int stt = 1;
        for (SanphamDTO sp : listSanpham) {
            // Only show active products with stock > 0
            if (sp.getTrang_thai() == 1 && sp.getSo_luong_ton() > 0) {
                tableModel.addRow(new Object[] {
                        stt++,
                        sp.getMa_sp(),
                        sp.getTenSP(),
                        sp.getMa_loai(),
                        sp.getChip(),
                        sp.getCard(),
                        sp.getRam(),
                        sp.getSo_luong_ton(),
                        sp.getGia_ban()
                });
            }
        }
    }

    private void searchSanPham() {
        String keyword = txtTimKiem.getText().trim().toLowerCase();

        // If keyword is empty, load all products
        if (keyword.isEmpty()) {
            loadSanPham();
            return;
        }

        // Clear the table
        tableModel.setRowCount(0);

        // Search products
        int stt = 1;
        for (SanphamDTO sp : listSanpham) {
            // Search by product ID, name, or category
            if (sp.getMa_sp().toLowerCase().contains(keyword) ||
                    sp.getTenSP().toLowerCase().contains(keyword) ||
                    sp.getMa_loai().toLowerCase().contains(keyword)) {

                // Only show active products with stock > 0
                if (sp.getTrang_thai() == 1 && sp.getSo_luong_ton() > 0) {
                    tableModel.addRow(new Object[] {
                            stt++,
                            sp.getMa_sp(),
                            sp.getTenSP(),
                            sp.getMa_loai(),
                            sp.getChip(),
                            sp.getCard(),
                            sp.getRam(),
                            sp.getSo_luong_ton(),
                            sp.getGia_ban()
                    });
                }
            }
        }
    }

