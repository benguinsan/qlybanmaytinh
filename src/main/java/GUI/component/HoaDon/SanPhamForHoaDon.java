    private void btnChonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnChonActionPerformed
        int selectedRow = tableSanPham.getSelectedRow();
        if (selectedRow == -1) {
            // No row selected
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Vui lòng chọn một sản phẩm từ bảng",
                    "Lỗi",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Get the selected product ID
        String maSP = tableSanPham.getValueAt(selectedRow, 1).toString();
        String tenSP = tableSanPham.getValueAt(selectedRow, 2).toString();
        int soLuongTon = Integer.parseInt(tableSanPham.getValueAt(selectedRow, 7).toString());
        double giaBan = Double.parseDouble(tableSanPham.getValueAt(selectedRow, 8).toString());

        // Get the quantity
        String soLuongText = txtSoLuongBan.getText().trim();

        // Validate inputs
        if (soLuongText.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Vui lòng nhập số lượng bán",
                    "Lỗi",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            txtSoLuongBan.requestFocus();
            return;
        }

        try {
            int soLuong = Integer.parseInt(soLuongText);
            if (soLuong <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Số lượng bán phải lớn hơn 0",
                        "Lỗi",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                txtSoLuongBan.requestFocus();
                return;
            }

            if (soLuong > soLuongTon) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Số lượng bán không thể lớn hơn số lượng tồn (" + soLuongTon + ")",
                        "Lỗi",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                txtSoLuongBan.requestFocus();
                return;
            }

            // Pass the selected product back to the AddHoaDon panel
            if (parentComponent instanceof AddHoaDon) {
                AddHoaDon addHoaDon = (AddHoaDon) parentComponent;
                addHoaDon.addProductToTable(maSP, tenSP, soLuong, giaBan);

                // Add debug message
                System.out.println("Product added: " + maSP + ", " + tenSP + ", " + soLuong + ", " + giaBan);
            } else {
                // Add debug message
                System.out.println("parentComponent is not an instance of AddHoaDon: " +
                        (parentComponent != null ? parentComponent.getClass().getName() : "null"));
            }

            // Close the dialog
            if (parentDialog != null) {
                parentDialog.dispose();
            } else {
                System.out.println("parentDialog is null");
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Số lượng bán phải là số nguyên",
                    "Lỗi",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            txtSoLuongBan.requestFocus();
        }
    }// GEN-LAST:event_btnChonActionPerformed

